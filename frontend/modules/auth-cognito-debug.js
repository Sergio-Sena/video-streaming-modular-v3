/**
 * M√≥dulo de Autentica√ß√£o com Amazon Cognito - Vers√£o Debug
 */
class AuthCognitoDebug {
    constructor() {
        console.log('üîê AuthCognitoDebug inicializado');
        this.currentScreen = 'login';
        this.cognitoConfig = {
            userPoolId: 'us-east-1_FpDtOqzEa',
            clientId: '6in4ghmp6k6vl09556a52ug7qj',
            region: 'us-east-1'
        };
        
        console.log('‚öôÔ∏è Cognito Config:', this.cognitoConfig);
        
        // Configurar AWS SDK
        if (typeof AWS !== 'undefined') {
            console.log('‚úÖ AWS SDK carregado');
            AWS.config.region = this.cognitoConfig.region;
            this.cognitoIdentityServiceProvider = new AWS.CognitoIdentityServiceProvider();
        } else {
            console.warn('‚ö†Ô∏è AWS SDK n√£o encontrado');
        }
        
        this.initEventListeners();
    }

    initEventListeners() {
        console.log('üéØ Configurando event listeners...');
        
        // Login form
        const loginForm = document.getElementById('loginForm');
        if (loginForm) {
            loginForm.addEventListener('submit', (e) => {
                e.preventDefault();
                console.log('üìù Form de login submetido');
                this.handleLogin();
            });
        }

        // Logout
        const logoutBtn = document.getElementById('logoutBtn');
        if (logoutBtn) {
            logoutBtn.addEventListener('click', () => {
                console.log('üö™ Logout clicado');
                this.logout();
            });
        }
        
        console.log('‚úÖ Event listeners configurados');
    }

    async handleLogin() {
        console.log('üîë Iniciando processo de login...');
        
        const email = document.getElementById('email').value;
        const password = document.getElementById('password').value;
        const errorDiv = document.getElementById('errorMessage');
        const loginBtn = document.querySelector('.login-btn');

        console.log('üìß Email:', email);
        console.log('üîí Senha:', password ? '***' : 'vazia');

        this.setLoading(loginBtn, true);
        if (errorDiv) errorDiv.textContent = '';

        try {
            console.log('üöÄ Chamando cognitoLogin...');
            const response = await this.cognitoLogin(email, password);
            console.log('üì• Resposta do login:', response);
            
            if (response.success) {
                console.log('‚úÖ Login bem-sucedido!');
                
                localStorage.setItem('authToken', response.token);
                localStorage.setItem('userEmail', email);
                
                const userEmailEl = document.getElementById('userEmail');
                if (userEmailEl) {
                    userEmailEl.textContent = email;
                }
                
                this.showSuccessMessage('‚úì Login realizado com sucesso!');
                
                console.log('üîÑ Mudando para tela principal...');
                this.showScreen('main');
                
                // Carregar v√≠deos com Cognito
                console.log('üé¨ Login conclu√≠do - carregando v√≠deos...');
                
                setTimeout(() => {
                    console.log('üìπ Tentando carregar v√≠deos com Cognito...');
                    if (window.videosModule && typeof window.videosModule.loadVideos === 'function') {
                        console.log('üéØ Chamando loadVideos...');
                        window.videosModule.loadVideos();
                    } else {
                        console.log('üìé VideosModule n√£o dispon√≠vel ainda');
                    }
                }, 500);
                
            } else {
                console.error('‚ùå Login falhou:', response.message);
                this.showError(response.message || 'Credenciais inv√°lidas');
            }
        } catch (error) {
            console.error('üí• Erro no login:', error);
            this.showError('Erro de autentica√ß√£o: ' + error.message);
        } finally {
            this.setLoading(loginBtn, false);
        }
    }

    async cognitoLogin(username, password) {
        console.log('üîê Tentando autentica√ß√£o Cognito...');
        
        try {
            if (!this.cognitoIdentityServiceProvider) {
                console.warn('‚ö†Ô∏è AWS SDK n√£o dispon√≠vel, usando fallback');
                throw new Error('AWS SDK n√£o carregado');
            }

            const params = {
                AuthFlow: 'USER_PASSWORD_AUTH',
                ClientId: this.cognitoConfig.clientId,
                AuthParameters: {
                    USERNAME: username,
                    PASSWORD: password
                }
            };

            console.log('üì§ Enviando para Cognito:', { ...params, AuthParameters: { USERNAME: username, PASSWORD: '***' } });
            
            const result = await this.cognitoIdentityServiceProvider.initiateAuth(params).promise();
            console.log('üì• Resposta Cognito:', result);
            
            if (result.AuthenticationResult) {
                return {
                    success: true,
                    token: result.AuthenticationResult.IdToken,
                    accessToken: result.AuthenticationResult.AccessToken,
                    refreshToken: result.AuthenticationResult.RefreshToken
                };
            } else {
                return { success: false, message: 'Falha na autentica√ß√£o' };
            }
        } catch (error) {
            console.error('üí• Erro Cognito:', error);
            
            // Tentar autentica√ß√£o direta com Cognito usando AWS SDK
            console.log('üîÑ Tentando autentica√ß√£o direta...');
            try {
                const cognitoIdentity = new AWS.CognitoIdentityServiceProvider();
                const authParams = {
                    AuthFlow: 'ADMIN_NO_SRP_AUTH',
                    UserPoolId: this.cognitoConfig.userPoolId,
                    ClientId: this.cognitoConfig.clientId,
                    AuthParameters: {
                        USERNAME: username,
                        PASSWORD: password
                    }
                };
                
                console.log('üì§ Tentando ADMIN_NO_SRP_AUTH...');
                const result = await cognitoIdentity.adminInitiateAuth(authParams).promise();
                
                if (result.AuthenticationResult) {
                    console.log('‚úÖ Autentica√ß√£o direta bem-sucedida!');
                    return {
                        success: true,
                        token: result.AuthenticationResult.IdToken,
                        accessToken: result.AuthenticationResult.AccessToken,
                        refreshToken: result.AuthenticationResult.RefreshToken
                    };
                }
            } catch (directError) {
                console.error('üí• Erro autentica√ß√£o direta:', directError);
            }
            
            // √öltimo fallback para teste
            if (username === 'sergiosenaadmin@sstech' && password === 'sergiosena') {
                console.log('‚ö†Ô∏è Usando fallback de teste...');
                return {
                    success: true,
                    token: 'test-token-' + Date.now()
                };
            }
            
            return { 
                success: false, 
                message: error.message || 'Credenciais inv√°lidas' 
            };
        }
    }

    checkAuth() {
        console.log('üîç Verificando autentica√ß√£o...');
        
        const token = localStorage.getItem('authToken');
        const email = localStorage.getItem('userEmail');
        
        console.log('üé´ Token:', token ? 'presente' : 'ausente');
        console.log('üìß Email:', email);
        
        if (token && email) {
            console.log('‚úÖ Usu√°rio j√° autenticado');
            
            const userEmailEl = document.getElementById('userEmail');
            if (userEmailEl) {
                userEmailEl.textContent = email;
            }
            
            this.showScreen('main');
            
            console.log('‚úÖ Usu√°rio j√° autenticado - carregando v√≠deos...');
            
            // Carregar v√≠deos quando j√° autenticado
            setTimeout(() => {
                console.log('üìπ Carregando v√≠deos para usu√°rio autenticado...');
                if (window.videosModule && typeof window.videosModule.loadVideos === 'function') {
                    console.log('üéØ Chamando loadVideos (usu√°rio j√° autenticado)...');
                    window.videosModule.loadVideos();
                } else {
                    console.log('üìé VideosModule n√£o dispon√≠vel ainda - tentando novamente...');
                    // Tenta novamente ap√≥s mais tempo
                    setTimeout(() => {
                        if (window.videosModule && typeof window.videosModule.loadVideos === 'function') {
                            console.log('üéØ Segunda tentativa loadVideos...');
                            window.videosModule.loadVideos();
                        }
                    }, 1000);
                }
            }, 500);
            
            return;
        }
        
        console.log('‚ùå Usu√°rio n√£o autenticado');
        this.showScreen('login');
    }

    logout() {
        console.log('üö™ Fazendo logout...');
        localStorage.removeItem('authToken');
        localStorage.removeItem('userEmail');
        this.showScreen('login');
        
        const loginForm = document.getElementById('loginForm');
        if (loginForm) loginForm.reset();
        
        const errorDiv = document.getElementById('errorMessage');
        if (errorDiv) {
            errorDiv.textContent = '';
            errorDiv.style.display = 'none';
        }
    }

    showScreen(screenName) {
        console.log('üñ•Ô∏è Mudando para tela:', screenName);
        
        document.querySelectorAll('.screen').forEach(screen => {
            screen.classList.remove('active');
        });
        
        const targetScreen = document.getElementById(`${screenName}Screen`);
        if (targetScreen) {
            targetScreen.classList.add('active');
            console.log('‚úÖ Tela ativada:', screenName);
        } else {
            console.error('‚ùå Tela n√£o encontrada:', screenName);
        }
        
        this.currentScreen = screenName;
    }

    setLoading(button, loading) {
        if (!button) return;
        
        const spinner = button.querySelector('.loading-spinner');
        const text = button.querySelector('span');
        
        if (loading) {
            button.disabled = true;
            if (spinner) spinner.style.display = 'block';
            if (text) text.style.opacity = '0.7';
        } else {
            button.disabled = false;
            if (spinner) spinner.style.display = 'none';
            if (text) text.style.opacity = '1';
        }
    }

    showError(message) {
        console.error('üö® Erro:', message);
        const errorDiv = document.getElementById('errorMessage');
        if (!errorDiv) return;
        
        errorDiv.textContent = message;
        errorDiv.style.display = 'block';
        errorDiv.style.backgroundColor = '#dc3545';
        errorDiv.style.color = 'white';
        errorDiv.style.padding = '10px';
        errorDiv.style.borderRadius = '5px';
        errorDiv.style.marginTop = '10px';
        
        setTimeout(() => {
            errorDiv.style.display = 'none';
        }, 5000);
    }

    showSuccessMessage(message) {
        console.log('‚úÖ Sucesso:', message);
        const errorDiv = document.getElementById('errorMessage');
        if (!errorDiv) return;
        
        errorDiv.textContent = message;
        errorDiv.style.display = 'block';
        errorDiv.style.backgroundColor = '#28a745';
        errorDiv.style.color = 'white';
        errorDiv.style.padding = '10px';
        errorDiv.style.borderRadius = '5px';
        errorDiv.style.marginTop = '10px';
        
        setTimeout(() => {
            errorDiv.style.display = 'none';
        }, 2000);
    }
}

// Exportar para uso global
window.AuthCognitoDebug = AuthCognitoDebug;